"""
Cuál es la diferencia entre un condicional simple y un
condicional compuesto?
"""

print(f"Un condicional simple es el que ejecuta el programa si la expresión insertada es booleana mediante un if. Un condicional compuesto es el que presenta una serie de instrucciones extras dentro de condicional y requiere una anidación de condicionales, if, else, else if, etc")